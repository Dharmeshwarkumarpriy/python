s="sam singh kashyap"
# bigin index : end index : start index. ......
print(s[1:7:1])
print(s[1:7])
print(s[:7])
print(s[7:])
print(s[1:7:2])
# if we are not specify the begin index the end index and the step then the slice operator print the whole string. ....
print(s[::])
print(s[:])
# step value should be +ve and -ve. ....
print(s[1:5:-1])
print(s[::-1])
print(s[5:2:-2])
print(s[1:7:2])
print(s[1:-5:1])
print(s[-1:-3:-1])